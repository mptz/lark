#define SIZEHINT 10

/*
 * When dumping stats, we parameterize some detailed information on a
 * HASHTEST_DETAILED_STATS environment variable; we don't want those
 * stats for automated test purposes, since they depend on the details
 * of the hash function and will vary between 32- and 64-bit hosts.
 */

static void dump_hashtab_stats(struct hashtab *hashtab)
{
	struct hashtab_stats hstats;
	hashtab_stats(hashtab, &hstats);
	printf("Hash table capacity %zu, used %zu\n",
		hstats.capacity, hstats.used);
	if (getenv("HASHTEST_DETAILED_STATS")) {
		printf("Hash table nests total %zu, used %zu\n",
		       hstats.nests, hstats.nestsused);
		for (size_t i = 0; i < CUCKOO_NEST_SIZE; ++i)
			printf("Nest usage for slot %zu: %zu\n", i,
				hstats.entry_used[i]);
	}

	/*
	 * Regardless of the actual numbers, we can assert some
	 * invariants which are very unlikely to be violated, unless
	 * we catch things just at the wrong time (e.g. after growth).
	 */
	assert((double) hstats.used / (double) hstats.capacity > 0.333);
	assert((double) hstats.used / (double) hstats.capacity < 0.666);
	assert((double) hstats.nestsused / (double) hstats.nests > 0.5);
	for (size_t i = 1; i < CUCKOO_NEST_SIZE; ++i)
		assert(hstats.entry_used[i-1] > hstats.entry_used[i]);

}

static void dump_wordtab_stats(struct wordtab *wordtab)
{

	struct wordtab_stats wstats;
	wordtab_stats(wordtab, &wstats);
	printf("Word table capacity %zu, used %zu, #bins %zu\n",
		wstats.capacity, wstats.used, wstats.bins);
	if (getenv("HASHTEST_DETAILED_STATS")) {
		printf("Word table bins used: %zu\n", wstats.binsused);
		for (size_t i = 0; i < CUCKOO_NEST_SIZE; ++i)
			printf("Nest usage for slot %zu: %zu\n", i,
				wstats.entry_used[i]);
	}
}

static void run_dict_test(void)
{
	const char cmd[] = "xzcat src/util/test/american-english.xz";
	FILE *input = popen(cmd, "r");
	if (!input)
		ppanic(cmd);

	struct hashtab hashtab;
	struct wordtab wordtab;
	hashtab_init(&hashtab, SIZEHINT);
	wordtab_init(&wordtab, SIZEHINT);

	/*
	 * For each dictionary-word, the hashtab entry points to an
	 * index, and the corresponding wordtab entry stores that
	 * dictionary-word's length.
	 */
	char buf [100];
	word nwords;
	for (nwords = 1; fgets(buf, sizeof buf, input); ++nwords) {
		size_t size = strlen(buf) + 1;
		hashtab_put(&hashtab, xstrdup(buf), size, (void*) nwords);
		wordtab_put(&wordtab, nwords, (void*) (size - 1));
	}
	--nwords;	/* we started at 1 */

	if (pclose(input))
		ppanic(cmd);

	/*
	 * Iterate over all words, lookup lengths, check for match.
	 */
	struct hashtab_iter iter;
	hashtab_iter_init(&hashtab, &iter);
	struct hashtab_entry *entry;
	while ((entry = hashtab_iter_next(&iter))) {
		word i = (word) entry->data;
		size_t len = (size_t) wordtab_get(&wordtab, i);
		if (len != strlen(entry->key))
			panic("Word length mismatch!\n");
	}

	/*
	 * Output stats... before checking in a new reference test output,
	 * we should always sanity-check the stats (e.g. capacity vs. used
	 * should be a reasonable ratio).
	 */
	dump_hashtab_stats(&hashtab);
	dump_wordtab_stats(&wordtab);

	/*
	 * Clean up tables including freeing all the strdup'd hash keys.
	 */
	hashtab_free_all_keys(&hashtab);
	hashtab_fini(&hashtab);
	wordtab_fini(&wordtab);
}

static void run_free_test(void)
{
	struct hashtab hashtab;
	int r;

	hashtab_init(&hashtab, SIZEHINT);
	for (size_t i = 0; i < sizeof elements / sizeof elements[0]; ++i)
		hashtab_put(&hashtab, xstrdup(elements[i]),
			    strlen(elements[i]), xstrdup(elements[i]));
	hashtab_free_all(&hashtab);
	hashtab_fini(&hashtab);

	void *oob = (void*) "oob";
	hashtab_init(&hashtab, SIZEHINT);
	r = hashtab_set_oob(&hashtab, oob);
	if (r) panic("hashtab_set_oob\n");
	for (size_t i = 0; i < sizeof elements / sizeof elements[0]; ++i)
		hashtab_put(&hashtab, xstrdup(elements[i]),
			    strlen(elements[i]), xstrdup(elements[i]));
	hashtab_free_all_keys(&hashtab);
	hashtab_free_all_data(&hashtab);
	hashtab_fini(&hashtab);
}

static void run_oob_test(void)
{
	char apple[] = "apple", banana[] = "banana", cherry[] = "cherry",
	     kumquat[] = "kumquat";
	void *oob = (void*) "oob", *p;
	int r;

	struct hashtab hashtab;
	hashtab_init(&hashtab, SIZEHINT);
	r = hashtab_set_oob(&hashtab, oob);
	if (r) panic("hashtab_set_oob\n");

	hashtab_put(&hashtab, apple, sizeof apple, apple);
	hashtab_put(&hashtab, banana, sizeof banana, banana);
	hashtab_put(&hashtab, cherry, sizeof cherry, cherry);

	p = hashtab_get(&hashtab, apple, sizeof apple);
	if (p != apple) panic("hashtab_get\n");
	hashtab_put(&hashtab, apple, sizeof apple, NULL);   /* should work */
	p = hashtab_get(&hashtab, apple, sizeof apple);
	if (p != NULL) panic("hashtab_get\n");
	p = hashtab_get(&hashtab, kumquat, sizeof kumquat);
	if (p != oob) panic("hashtab_get\n");

	/* Force growth of table with non-default OOB value */
	for (size_t i = 0; i < sizeof planets / sizeof planets[0]; ++i)
		hashtab_put(&hashtab, planets[i], strlen(planets[i]) + 1,
			    (void*) planets[i]);

	p = hashtab_get(&hashtab, apple, sizeof apple);
	if (p != NULL) panic("hashtab_get\n");

	r = hashtab_set_oob(&hashtab, NULL); /* should fail, datum for apple */
	if (r == 0) panic("hashtab_set_oob\n");
	hashtab_put(&hashtab, apple, sizeof apple, apple);
	r = hashtab_set_oob(&hashtab, NULL); /* now it should work */
	if (r) panic("hashtab_set_oob\n");
	r = hashtab_set_oob(&hashtab, NULL); /* redundant calls should work */
	if (r) panic("hashtab_set_oob\n");

	p = hashtab_get(&hashtab, apple, sizeof apple);
	if (p != apple) panic("hashtab_get\n");
	p = hashtab_get(&hashtab, banana, sizeof banana);
	if (p != banana) panic("hashtab_get\n");
	p = hashtab_get(&hashtab, cherry, sizeof cherry);
	if (p != cherry) panic("hashtab_get\n");

	dump_hashtab_stats(&hashtab);
	hashtab_fini(&hashtab);
}

int main(int argc, char *argv[])
{
	set_execname(argv[0]);
	run_dict_test();
	run_free_test();
	run_oob_test();
	return 0;
}
